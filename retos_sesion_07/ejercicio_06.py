def otros_ejemplos():
    ejemplo = "Esto es un ejemplo"
    # Ejemplo 1: Convertir a mayúsculas
    print("Mayúsculas:", ejemplo.upper())
    # Ejemplo 2: Reemplazar texto
    print("Reemplazar:", ejemplo.replace("ejemplo", "otro ejemplo"))
    # Ejemplo 3: Contar ocurrencias
    print("Contar ocurrencias de 'e':", ejemplo.count("e"))
    # Ejemplo 4: Separar cadena en palabras
    print("Separar palabras:", ejemplo.split())
    # Ejemplo 5: Concatenar cadenas
    print("Concatenar:", "¡Hola, " + "mundo!")

if __name__ == "__main__":
    otros_ejemplos()