def escribir_poema_dia_madre():
    poema = """En este día tan especial,
queremos a mamá homenajear.
Con amor y gratitud sin igual,
su cariño y sacrificio recordar.

Eres luz en nuestros días oscuros,
eres fuerza cuando flaqueamos.
Tu amor incondicional es puro,
en tus brazos siempre nos refugiamos.

En cada gesto y en cada mirada,
se refleja tu ternura sin par.
Madre, eres nuestra eterna morada,
nuestro refugio, nuestro hogar.

Hoy te decimos con todo el corazón,
¡Feliz Día de la Madre, con amor!"""
    print(poema)

if __name__ == "__main__":
    escribir_poema_dia_madre()
